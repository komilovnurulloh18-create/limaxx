function applyLanguage(lang) {
  const safeLang = translations[lang] ? lang : "uz";
  const dict = translations[safeLang];

  document.documentElement.lang = safeLang;
  localStorage.setItem("limax_language", safeLang);

  document.querySelectorAll("[data-lang]").forEach((element) => {
    const key = element.dataset.lang;
    if (dict[key]) {
      if (element.dataset.langHtml === "true") {
        element.innerHTML = dict[key];
      } else if (element.tagName === "INPUT") {
        element.placeholder = dict[key];
      } else {
        element.textContent = dict[key];
      }
    }
  });

  document.querySelectorAll(".language-selector").forEach((select) => {
    select.value = safeLang;
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("limax_language") || "uz";
  applyLanguage(saved);

  document.querySelectorAll(".language-selector").forEach((select) => {
    select.addEventListener("change", (event) => {
      applyLanguage(event.target.value);
    });
  });
});
